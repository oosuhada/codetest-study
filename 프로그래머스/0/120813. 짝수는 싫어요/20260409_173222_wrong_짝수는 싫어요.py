# 2. ✅ 실수 수정 버전 (내 방식 유지)
# =========================================================

def solution(n):
    answer = []
    for i in range(0, n + 1):   # range()는 함수, 인자는 쉼표로 구분
        if i % 2 != 0:           # 홀수 조건: 2로 나눈 나머지가 0이 아님
            answer.append(i)     # 리스트에 원소 추가는 .append()
    return answer

