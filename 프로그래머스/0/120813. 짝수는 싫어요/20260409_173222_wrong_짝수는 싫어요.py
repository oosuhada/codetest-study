# (1) for문 문법 자체를 모름 → for i in range(...): 형태여야 함
# for i < n:  ❌

# (2) for i in n < n: → 문법 자체가 존재하지 않음
# for i in n < n:  ❌

# (3) 파이썬에는 !== 연산자가 없음 → !=  써야 함 (JS 습관)
# if i % 2 !== 0:  ❌

# (4) (i+1) % 2 == 0 은 "i가 홀수" 조건이 아님
#     → i+1이 짝수 == i가 홀수 라서 결과는 맞지만
#     → i % 2 != 0 (또는 == 1) 이 훨씬 직관적
# if (i+1) % 2 == 0:  △ (작동은 되지만 비직관적)

# (5) &&는 파이썬 문법이 아님 → and 써야 함 (JS 습관)
# if i < n && ...:  ❌

# (6) for i in n: → n은 정수라서 이터러블이 아님
# for i in n:  ❌

# (7) range() 문법을 잘못 씀 → 콜론(:)이 아니라 쉼표(,)
# for i in range(0:n+1):  ❌

# (8) range[]로 대괄호 사용 → range()는 함수라서 () 써야 함
# for i in range[0:(n+1)]:  ❌

# (9) answer += i → 리스트에 정수를 += 하면 에러
#     리스트에 원소 추가할 때는 .append() 또는 += [i]
# answer += i  ❌

# (10) range(0, n+1)에서 i <= n 조건이 중복됨
#      range가 이미 0~n까지만 돌기 때문에 if i <= n은 불필요
# for i in range(0, n+1): if i <= n and ...:  △ (작동은 되지만 중복)


