# ✔ range(start, stop)
#   - start 이상 stop 미만의 정수를 순서대로 생성
#   - n 이하까지 포함하려면 range(1, n+1) 사용
#   예:
#   list(range(1, 6)) → [1, 2, 3, 4, 5]

# ✔ % (모듈로 연산자)
#   - 나눗셈의 나머지를 반환
#   - 짝수: i % 2 == 0
#   - 홀수: i % 2 != 0  (또는 i % 2 == 1)
#   예:
#   5 % 2 → 1  (홀수)
#   4 % 2 → 0  (짝수)

# ✔ list.append(x)
#   - 리스트 끝에 원소 x를 추가
#   - 반환값: None (sort()처럼 원본을 직접 수정)
#   예:
#   arr = [1, 3]
#   arr.append(5)
#   print(arr) → [1, 3, 5]

# ✔ 리스트 컴프리헨션
#   - [표현식 for 변수 in 이터러블 if 조건]
#   - for + if + append 를 한 줄로 줄일 수 있음
#   예:
#   [i for i in range(1, 11) if i % 2 != 0]
#   → [1, 3, 5, 7, 9]