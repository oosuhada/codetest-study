# =========================================================


# 1. 실수 수정 버전 (내 코드 기반 수정)
def solution(n):
    
    result = []
    x = n
    
    result.append(x)  # 🔥 시작값 먼저 추가
    
    while x > 1:  # 🔥 for → while (조건 반복)
        
        if x % 2 == 0:
            x = x // 2  # 🔥 / → // (정수 유지)
        else:
            x = (3 * x) + 1
        
        result.append(x)  # 🔥 계산 후 추가
    
    return result


