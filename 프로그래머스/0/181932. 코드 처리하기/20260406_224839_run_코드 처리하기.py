def solution(code):
    
    result = ''   # 결과 문자열
    mode = 0      # 시작 mode는 0
    
    # =========================================================
    # 🔴 [실수 1] for문 콜론(:) 빠짐
    # for idx in range(len(code))   ❌
    # 👉 Python에서 for/if 뒤에는 반드시 : 필요
    # =========================================================
    
    # =========================================================
    # 🔴 [실수 2] 인덱스 순회 개념 부족
    #
    # ❌ for idx in code:
    # 👉 idx = "a", "b", "c" (문자)
    #
    # ✅ for idx in range(len(code)):
    # 👉 idx = 0,1,2,3 (인덱스)
    #
    # 👉 이 문제는 "짝수/홀수 idx"가 중요하기 때문에
    # 반드시 인덱스 순회를 해야 한다
    # =========================================================
    
    for idx in range(len(code)):  # ✅ 올바른 인덱스 순회
        
        current_char = code[idx]  # 🔴 [실수 3] code[idx]로 접근해야 함
        
        # =====================================================
        # 🔴 [실수 4] 문자열 비교 실수 ("1" vs 1)
        #
        # ❌ code[idx] == 1
        # 👉 code는 문자열이므로 "1"과 비교해야 함
        #
        # ✅ code[idx] == "1"
        # =====================================================
        
        if current_char == "1":
            
            # =================================================
            # 🔴 [실수 5] 들여쓰기 붕괴
            #
            # ❌
            # if code[idx] == "1":
            # mode = 1 - mode
            #
            # 👉 Python은 들여쓰기로 블록을 구분
            # =================================================
            
            mode = 1 - mode   # mode 전환 (0 ↔ 1)
            continue          # 🔴 "1"은 결과에 추가하지 않음
        
        # =====================================================
        # 🔴 [실수 6] if문이 for문 밖에 있었음
        #
        # ❌
        # for:
        #
        # if mode == 0:
        #
        # 👉 반복하면서 조건 검사해야 하므로
        # 반드시 for 안에 있어야 한다
        # =====================================================
        
        # mode가 0일 때
        if mode == 0:
            
            # =================================================
            # 🔴 [실수 7] 잘못된 else 구조
            #
            # ❌
            # if idx % 2 == 0:
            #     ref += code[idx]
            # else:
            #     ref += code[idx]
            #
            # 👉 이건 그냥 무조건 추가하는 코드임
            # =================================================
            
            if idx % 2 == 0:
                result += current_char
        
        # mode가 1일 때
        else:
            if idx % 2 == 1:
                result += current_char
    
    # =========================================================
    # 🔴 [실수 8] EMPTY 조건 잘못 이해
    #
    # ❌ if len(code) == 0
    # 👉 입력이 아니라 "결과"가 비어야 함
    #
    # ✅ if result == ''
    # =========================================================
    
    if result == '':
        return "EMPTY"
    
    return result