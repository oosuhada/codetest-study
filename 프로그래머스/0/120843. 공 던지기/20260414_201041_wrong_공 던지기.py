# 접근법 2: 짝수/홀수 경우의 수 나누기 방식
# ============================================================
 
def solution_v2(numbers, k):
    n = len(numbers)
 
    # 핵심 아이디어:
    # 한 바퀴(1사이클)에 몇 명이 공을 받는가?
    #   - n이 짝수: n/2 명 (예: [1,2,3,4] → 1,3,1,3... → 2명 사이클)
    #   - n이 홀수: n 명 (예: [1,2,3] → 1,3,2,1,3,2... → 3명 사이클)
    #     홀수일 때 2바퀴를 돌면 전원이 한 번씩 받고 원래 패턴으로 돌아옴
 
    if n % 2 == 0:
        # 짝수: 사이클 길이 = n // 2
        # ex) [1,2,3,4] 사이클: 1→3→1→3... (2명)
        cycle = n // 2
        idx = ((k - 1) % cycle) * 2
        return numbers[idx]
 
    else:
        # 홀수: 사이클 길이 = n
        # ex) [1,2,3] 사이클: 1→3→2→1→3→2... (3명)
        # 1바퀴(홀수번): 인덱스 0, 2, 4, 1, 3 ... (짝수 인덱스 먼저, 그 다음 홀수 인덱스)
        # 2바퀴(짝수번): 마찬가지로 이어짐
        #
        # 홀수 n일 때 순서를 직접 추적하면:
        # k=1: idx=0  k=2: idx=2  k=3: idx=4(=1, n=3이므로)→ idx=1
        # 즉 idx = ((k-1)*2) % n 과 동일 → 사실 짝수/홀수 구분 불필요!
        #
        # 그래도 경우의 수 방식으로 완성하면:
        cycle = n  # 홀수일 때 사이클은 n
        # k가 사이클 내 몇 번째인지
        pos = (k - 1) % cycle  # 0-based 위치
        idx = (pos * 2) % n
        return numbers[idx]
 
 
