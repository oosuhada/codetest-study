# 접근법 1: numbers*2 방식
# ============================================================
 
def solution_v1(numbers, k):
    # numbers*2를 하면 왜 런타임 에러가 날까?
    # numbers의 최대 길이는 99 (문제 조건: 2 < len < 100)
    # numbers*2 해도 최대 길이 198
    # k는 최대 999 → (k-1)*2 = 최대 1996
    # 1996 > 198 → 인덱스 범위 초과! → IndexError (런타임 에러)
    #
    # 해결: 배열을 충분히 크게 만들면 된다
    # (k-1)*2 최대값이 1996이므로, 배열 길이가 1997 이상이면 OK
    # numbers * 몇 배? → 1997 / len(numbers) 올림만큼 곱하면 충분
    # 하지만 가장 단순하게는 k번 반복할 수 있을 만큼만 크게 만들기
 
    import math
    # (k-1)*2 가 최대 인덱스 → 배열 길이가 이것보다 커야 함
    # 몇 배 복사해야 할지 = ((k-1)*2 + 1) / len(numbers) 올림
    repeat = math.ceil(((k - 1) * 2 + 1) / len(numbers))
    new_numbers = numbers * repeat  # 딱 필요한 만큼만 복사
 
    idx = (k - 1) * 2  # k번째로 받는 사람의 인덱스
    return new_numbers[idx]
 
 
