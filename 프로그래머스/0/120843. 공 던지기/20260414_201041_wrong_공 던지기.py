# 내가 놓친 부분 / 잘못 알고 있던 부분 정리
# ============================================================
 
# [1] numbers*2가 런타임 에러가 난 이유
#     numbers 최대 길이 = 99
#     numbers*2 = 최대 198개짜리 배열
#     k 최대 = 999 → 인덱스 (k-1)*2 = 1996
#     198개 배열에서 인덱스 1996에 접근 → IndexError!
#     → 배열을 충분히 크게 (k에 비례해서) 복사해야 한다
 
# [2] 짝수/홀수 경우를 나눠야 한다고 생각한 이유
#     "홀수 배열은 한 바퀴 돌 때 건너뛰는 패턴이 달라진다"는 직관은 맞음
#     하지만 결국 인덱스 공식 ((k-1)*2) % n 하나로 통합됨
#     % n (나머지 연산) 이 자동으로 원형 배열을 처리해주기 때문
 
# [3] 연산자 우선순위 실수
#     len(numbers)+1 // 2  →  실제로는  len(numbers) + (1//2) = len(numbers) + 0
#     의도한 것:  (len(numbers)+1) // 2
#     괄호를 반드시 써야 한다!
 
# [4] 원형 배열(circular array)을 다룰 때 핵심 도구
#     나머지 연산(%) 을 쓰면 배열 끝에서 자동으로 처음으로 돌아옴
#     idx = 어떤_인덱스 % 배열길이
#     이걸 알면 짝수/홀수 분기, 바퀴 수 계산이 전부 필요 없어짐
 
 
# ============================================================
# 깔끔한 풀이 (권장)
# ============================================================
 
def solution(numbers, k):
    n = len(numbers)
 
    # k번째로 공을 받는 사람의 인덱스를 구한다
    # - 공은 매번 2칸씩 건너뜀 (한 명 건너뛰고 그다음 사람)
    # - k=1이면 인덱스 0 (첫 번째 사람이 1번째로 던짐)
    # - k=2이면 인덱스 2 (2칸 이동)
    # - k번째이면 인덱스 (k-1)*2
    # - 배열이 원형이므로 % n으로 범위를 배열 안에 맞춤
 
    idx = ((k - 1) * 2) % n
    return numbers[idx]
 
 