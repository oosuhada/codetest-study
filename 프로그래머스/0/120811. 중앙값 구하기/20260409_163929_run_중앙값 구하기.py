# 🔥 sort() vs sorted() 헷갈림
# 🔥 sort()는 반환값이 None이라는 점
# 🔥 리스트 접근할 때 () 쓰는 실수
# 🔥 메서드인지 함수인지 구분 못함


# =========================================================
# 5. 🧠 핵심 개념 정리
# =========================================================

# ✔ sort()
# - 리스트 메서드
# - 원본 리스트를 직접 정렬
# - 반환값: None

# 예:
# arr = [3, 1, 2]
# arr.sort()
# print(arr) → [1, 2, 3]


# ✔ sorted()
# - 내장 함수
# - 정렬된 새로운 리스트 반환
# - 원본 유지됨

# 예:
# arr = [3, 1, 2]
# new_arr = sorted(arr)
# print(new_arr) → [1, 2, 3]
# print(arr) → [3, 1, 2]


