# =========================================================
# 1. ❌ 내가 했던 실수들
# =========================================================

# (1) sort()의 반환값을 변수에 담음 → None
# new_array = array.sort()  ❌

# (2) len(array.sort()) → len(None) 에러
# m = len(array.sort()) // 2  ❌

# (3) array.sort()[m] → None[m] 에러
# answer = array.sort()[m]  ❌

# (4) array.sorted() → 존재하지 않는 메서드
# array.sorted()  ❌

# (5) 리스트 접근을 ()로 함
# new_array(m)  ❌ → new_array[m]  ⭕


# =========================================================
# 2. ✅ 실수 수정 버전 (내 방식 유지)
# =========================================================

def solution_fix(array):
    array.sort()  # 원본 리스트 정렬
    m = len(array) // 2
    return array[m]


# =========================================================
# 3. 🚀 개선된 추천 스타일 (가장 깔끔)
# =========================================================

def solution_best(array):
    return sorted(array)[len(array) // 2]


