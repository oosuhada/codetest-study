# 문제: start_num부터 end_num까지 숫자 리스트 만들기
# =========================================================


# 1. 실수 수정 버전 (+= 사용)
def solution(start_num, end_num):
    
    answer = []
    
    for i in range(start_num, end_num+1):
        answer += [i]  # 🔥 리스트로 감싸서 추가
    
    return answer


