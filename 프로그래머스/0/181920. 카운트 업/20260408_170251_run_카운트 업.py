# ---------------------------------------------------------

# 👉 += 는 리스트 "확장" 개념

# answer += [i]

# 👉 리스트를 이어붙이는 것과 같음

# 예시:
# answer = [1,2]
# answer += [3]
# → [1,2,3]


# =========================================================
# [개념 2] answer += [i] 의미
# =========================================================

# 👉 리스트를 "확장(extend)" 하는 동작

# 내부적으로는:
# answer.extend([i]) 와 동일

# 즉:
# answer += [i]
# 👉 리스트 안의 요소를 하나씩 추가


# =========================================================
# [개념 3] append() vs += 차이 ⭐⭐⭐
# =========================================================

# 1️⃣ append
# answer.append(i)

# 👉 값 하나를 그대로 추가

# 예시:
# answer = [1,2]
# answer.append(3)
# → [1,2,3]


# ---------------------------------------------------------

# 2️⃣ += (extend 개념)
# answer += [i]

# 👉 리스트를 펼쳐서 추가

# 예시:
# answer = [1,2]
# answer += [3,4]
# → [1,2,3,4]


# ---------------------------------------------------------

# 🔥 차이 핵심

# append:
# 리스트를 "통째로" 넣음

# += :
# 리스트를 "풀어서" 넣음


# ---------------------------------------------------------

# 💥 중요한 차이 예시

# answer = []

# answer.append([1,2])
# → [[1,2]]  (리스트 안에 리스트)

# answer += [1,2]
# → [1,2]    (그대로 펼쳐짐)


