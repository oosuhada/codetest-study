# --------------------------------------------------
# 방법 1. while 반복문 (내가 쓴 방식)
# --------------------------------------------------
def factorial_while(n):
    result = 1
    while n >= 1:
        result *= n
        n -= 1
    return result

# --------------------------------------------------
# 방법 2. for 반복문 (더 파이써닉)
# --------------------------------------------------
def factorial_for(n):
    result = 1
    for i in range(1, n + 1):
        result *= i
    return result

