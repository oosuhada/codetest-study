# ============================================================
# ✅ 정답 풀이 1 - all() 활용 (소수 판별을 명확하게)
# ============================================================
 
def solution_v1(n):
    answer = []
    for c in range(2, n + 1):
        if n % c == 0:                                  # c가 n의 약수인지 먼저 확인
            if all(c % i != 0 for i in range(2, c)):   # c가 소수인지 확인
                answer.append(c)
    return answer
 
# 핵심 포인트:
#   all(조건 for i in range(...))
#   → range 안의 모든 i에 대해 조건이 전부 True일 때만 True 반환
#   → c=4: i=2일 때 4%2==0 → 조건 False → all() 전체가 False → append 안 됨 ✅
#   → c=3: i=2일 때 3%2=1!=0 → True → all() True → append ✅
#
# 주의: c=2 이면 range(2, 2) → 빈 범위 → all(빈 것) = True
#   → 2는 소수이므로 올바르게 append 됨 ✅
 
 
# ============================================================
# ✅ 정답 풀이 2 - n을 직접 나눠가는 방식 (가장 효율적, 추천!)
# ============================================================
 
def solution_v2(n):
    answer = []
    i = 2                   # 가장 작은 소수부터 시작
 
    while i <= n:
        if n % i == 0:      # i가 현재 n을 나눌 수 있으면
            answer.append(i)            # 소인수 목록에 추가
            while n % i == 0:          # i로 나눌 수 있는 만큼 전부 나눔
                n //= i                # n을 계속 줄여나감
        i += 1              # 다음 후보로 이동
 
    return answer
 
# 핵심 아이디어:
#   n을 소인수로 계속 나눠버리면 합성수가 자동으로 걸러진다!
#
#   예) n=12 과정:
#     i=2: 12%2==0 → answer=[2], 12→6→3 (2로 나눌 수 있는 만큼 나눔)
#     i=3: 3%3==0  → answer=[2,3], 3→1
#     i=4: 1%4!=0  → 스킵
#     결과: [2, 3] ✅
#
#   예) n=420 과정:
#     i=2: 420→210→105, answer=[2]
#     i=3: 105→35,      answer=[2,3]
#     i=4: 35%4!=0      스킵
#     i=5: 35→7,        answer=[2,3,5]
#     i=6: 7%6!=0       스킵
#     i=7: 7→1,         answer=[2,3,5,7]
#     결과: [2, 3, 5, 7] ✅
#
#   왜 합성수가 걸러질까?
#     i=4가 될 때, n은 이미 2로 나눌 수 있는 만큼 전부 나뉜 상태
#     → 4=2*2이므로, 4의 배수는 이미 2에서 처리됨 → 4는 절대 n을 못 나눔
#     → 소수 판별 없이도 소인수만 남는다!
 
 
