def solution(numbers):
    answer0 = numbers.replace("zero","0")
    answer1 = numbers0.replace("one","1")
    answer2 = numbers1.replace("two","2")
    answer3 = numbers2.replace("three","3")
    answer4 = numbers3.replace("four","4")
    answer5 = numbers4.replace("five","5")
    answer6 = numbers5.replace("six","6")
    answer7 = numbers6.replace("seven","7")
    answer8 = numbers7.replace("eight","8")
    answer9 = numbers8.replace("nine","9")
    
    return int(answer9)