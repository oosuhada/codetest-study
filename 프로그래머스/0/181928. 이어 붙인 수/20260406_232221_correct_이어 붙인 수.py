def solution(num_list):
    
    # num_list의 홀수만 이어붙인 문자열
    odd_list = ""
    # num_list의 짝수만 이어붙인 문자열
    even_list = ""
    
    for i in num_list:
        
        # 🔴 [개념] i는 리스트의 "값" (예: 3, 4, 5)
        
        if i % 2 == 1:
            
            # 🔴 [실수 1] odd_list(i) ❌
            # 👉 문자열 변수는 함수가 아님 (호출 불가)
            # 👉 odd_list(i)는 "함수 호출"처럼 해석됨 → 에러
            
            # 🔴 [실수 2] 붙여야 하는 건 odd_list가 아니라 i
            # ❌ str(odd_list(i))
            # 👉 현재 숫자 i를 문자열로 바꿔서 붙여야 함
            
            odd_list += str(i)   # ✅ 수정
        
        else:
            
            # 🔴 [실수 3] even_list(i) ❌ (위와 동일한 문제)
            even_list += str(i)  # ✅ 수정
    
    # 🔴 [개념] 문자열 → 정수 변환 후 더하기
    answer = int(odd_list) + int(even_list)
    
    return answer


