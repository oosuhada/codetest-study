def solution_advanced(num_list):
    
    # 🔴 join + 조건문 + 반복문 한 번에 처리
    odd = ''.join(str(i) for i in num_list if i % 2 == 1)
    even = ''.join(str(i) for i in num_list if i % 2 == 0)
    
    return int(odd) + int(even)


