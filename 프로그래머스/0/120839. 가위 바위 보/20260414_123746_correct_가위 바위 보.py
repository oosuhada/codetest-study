# enumerate 압축         | 값 → 인덱스 정규화 (좌표 압축)

# ============================================================
# 📋 유형별 딕셔너리 활용 요약
# ============================================================
# 문제 유형              | 딕셔너리 활용
# -----------------------|-------------------------------------------
# 두 수의 합 (Two Sum)   | {값: 인덱스} 저장 후 O(1) 탐색
# 애너그램 판별          | Counter 비교
# 최다 빈도 원소         | Counter.most_common(k)
# 연속 부분합            | {prefix_sum: 횟수}
# BFS/DFS                | defaultdict(list) 인접 리스트

