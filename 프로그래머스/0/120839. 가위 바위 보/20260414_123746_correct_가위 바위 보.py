# 3. 투 포인터 / 슬라이딩 윈도우 (prefix sum + dict)
# --------------------------------------------------
def count_subarrays(nums, k):
    count = defaultdict(int)
    count[0] = 1
    result = prefix = 0
    for n in nums:
        prefix += n
        result += count[prefix - k]
        count[prefix] += 1
    return result


