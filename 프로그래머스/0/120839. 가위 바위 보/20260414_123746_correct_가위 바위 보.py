# 2. 그룹핑 (defaultdict)
# --------------------------------------------------
from collections import defaultdict

groups = defaultdict(list)
pairs = [('a', 1), ('b', 2), ('a', 3)]
for k, v in pairs:
    groups[k].append(v)
# {'a': [1, 3], 'b': [2]}


