# --------------------------------------------------
# 2. 문자열 내 여러 단어 일괄 치환
# --------------------------------------------------
def replace_all(text, replace_map):
    for old, new in replace_map.items():
        text = text.replace(old, new)
    return text

replace_map = {'apple': 'orange', 'cat': 'dog', 'I': 'You'}
text = "I have a cat and an apple"
print(replace_all(text, replace_map))
# "You have a dog and an orange"
# ⚠️ 단, 연쇄 치환 문제 있음 → 문자 하나씩 바꿀 땐 아래 translate 사용


# --------------------------------------------------
# 3. str.translate() — 문자 단위 치환 (가장 빠름)
# --------------------------------------------------
table = str.maketrans({'a': 'A', 'e': 'E', 'i': 'I', 'o': 'O', 'u': 'U'})
text = "hello world"
print(text.translate(table))   # "hEllO wOrld"

# 문자 제거도 가능 (3번째 인자 = 삭제할 문자)
table2 = str.maketrans('', '', '!@#$%')
print("h@e#l!lo".translate(table2))   # "hello"


# --------------------------------------------------
# 4. 리스트 원소 치환 (★ 코테에서 가장 자주 사용!)
# --------------------------------------------------
