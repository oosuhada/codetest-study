# ============================================================
# 오답노트: 가위바위보 이기기 (딕셔너리 Replace 활용)
# ============================================================
# 문제 설명
# 가위는 2, 바위는 0, 보는 5로 표현합니다.
# rsp에 저장된 가위바위보를 모두 이기는 경우를 순서대로 나타낸 문자열을 return하세요.
#
# 입출력 예:
# "2"   → "0"
# "205" → "052"
# ============================================================

def solution(rsp):
    # rsp.replace() 방식의 문제점:
    # 이미 변환된 값이 또 변환될 수 있음!
    # 예) "2" → "0" → "5" 처럼 연쇄 변환 발생

    # ✅ 딕셔너리 방식으로 해결
    # 2(가위) → 0(바위), 0(바위) → 5(보), 5(보) → 2(가위)
    replace_map = {"2": "0", "0": "5", "5": "2"}

    # 각 문자를 독립적으로 변환하므로 연쇄 변환 문제 없음
    answer_list = [replace_map.get(n, n) for n in rsp]
    answer = ''.join(answer_list)
    return answer

# 테스트
print(solution("2"))    # "0"
print(solution("205"))  # "052"


