# 4. 메모이제이션 (DP 캐싱)
# --------------------------------------------------
from functools import lru_cache

# 자동 캐싱
@lru_cache(maxsize=None)
def fib_auto(n):
    if n <= 1: return n
    return fib_auto(n-1) + fib_auto(n-2)

# 수동 캐싱
cache = {}
def fib(n):
    if n in cache: return cache[n]
    if n <= 1: return n
    cache[n] = fib(n-1) + fib(n-2)
    return cache[n]


