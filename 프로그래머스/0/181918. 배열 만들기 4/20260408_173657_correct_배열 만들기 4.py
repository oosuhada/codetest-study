# =========================================================
# 문제: stk 스택 구성 (조건에 따라 push / pop 반복)
# =========================================================


# 1. 실수 수정 버전 (슬라이싱으로 제거)
def solution(arr):
    
    stk = []
    i = 0
    
    while i < len(arr):
        
        if len(stk) == 0:
            stk.append(arr[i])
            i += 1
        
        elif stk[-1] < arr[i]:
            stk.append(arr[i])
            i += 1
        
        else:
            stk = stk[0:len(stk)-1]  # 🔥 마지막 원소 제거 (슬라이싱)
    
    return stk


# ---------------------------------------------------------

# 2. 개선된 추천 스타일 (pop 사용 ⭐)
def solution_pop(arr):
    
    stk = []
    i = 0
    
    while i < len(arr):
        
        if not stk:  # 🔥 빈 리스트 체크 간결하게
            stk.append(arr[i])
            i += 1
        
        elif stk[-1] < arr[i]:
            stk.append(arr[i])
            i += 1
        
        else:
            stk.pop()  # 🔥 O(1)로 마지막 제거
    
    return stk


# ---------------------------------------------------------

# 3. 더 파이썬스러운 버전 (while 내부 최적화)
def solution_clean(arr):
    
    stk = []
    
    for num in arr:
        
        while stk and stk[-1] >= num:  # 🔥 조건 만족할 때까지 pop
            stk.pop()
        
        stk.append(num)
    
    return stk


