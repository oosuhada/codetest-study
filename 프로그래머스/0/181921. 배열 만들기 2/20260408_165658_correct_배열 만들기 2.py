# 문제: l 이상 r 이하에서 "0과 5로만 이루어진 정수" 찾기
# =========================================================


# 1. 실수 수정 + 내 방식 발전 버전
def solution(l, r):
    
    result = []
    
    for x in range(l, r+1):
        s = str(x)
        
        valid = True  # 🔥 이 숫자가 조건을 만족하는지 체크하는 스위치
        
        for c in s:
            if c not in ['0', '5']:  # 🔥 핵심 조건
                valid = False        # 조건 깨지면 False로 변경
                break               # 더 볼 필요 없음 → 바로 탈락
        
        if valid:
            result.append(x)
    
    return result if result else [-1]


