# =========================================================
# 문제: l 이상 r 이하에서 "0과 5로만 이루어진 정수" 찾기
# =========================================================


# 1. 실수 수정 + 내 방식 발전 버전
def solution(l, r):
    
    result = []
    
    for x in range(l, r+1):
        s = str(x)
        
        valid = True
        
        for c in s:
            if c in ['2','3','4','6','7','8','9']:
                valid = False
                break
        
        if valid:
            result.append(x)
    
    return result if result else [-1]  # 🔥 핵심 문법 1


# ---------------------------------------------------------

# 2. 개선된 추천 스타일
def solution_pythonic(l, r):
    
    result = []
    
    for x in range(l, r+1):
        s = str(x)
        
        if all(c == '0' or c == '5' for c in s):
            result.append(x)
    
    return result if result else [-1]


# ---------------------------------------------------------

# 3. 고급 버전 (set 활용)
def solution_advanced(l, r):
    
    result = []
    
    for x in range(l, r+1):
        if set(str(x)) <= {'0', '5'}:  # 🔥 핵심 문법 2
            result.append(x)
    
    return result if result else [-1]


