# ============================================================
# 문제: n 이하의 합성수 개수 구하기
# 합성수 = 약수의 개수가 세 개 이상인 수
# 제한: 1 ≤ n ≤ 100
# ============================================================
# 입출력 예)
#   n=10  → 5   (합성수: 4, 6, 8, 9, 10)
#   n=15  → 8   (합성수: 4, 6, 8, 9, 10, 12, 14, 15)
# ============================================================


# ────────────────────────────────────────────────────────────
# [❌ 오답 코드] - 내가 처음 작성한 풀이
# ────────────────────────────────────────────────────────────

def solution_wrong(n):
    # 풀이 방향은 맞았음:
    # 10 이하 소수 -> 1,2,3,5,7
    # 합성수 개수 = n - (n이하 소수 개수)
    # 1 < c < n 중 n % c == 0 이 있다면 합성수, 없다면 소수

    verifier = 0
    answer = 0
    x = 2

    # [❌ 버그 1] range[2, x+1] → range는 클래스이므로 [] 가 아닌 () 를 써야 함
    #            TypeError: 'type' object is not subscriptable 발생
    while x <= n:
        for c in range(2, x+1):         # ← range[] 에서 range() 로 수정은 했지만...
            if x % c == 0:
                verifier = 1
                x += 1                  # [❌ 버그 2] x += 1 이 for 루프 안에 있음
                                        #            약수를 찾을 때마다 x가 증가해버려
                                        #            숫자를 건너뛰거나 무한루프 발생
                if verifier == 1:
                    answer += 1         # [❌ 버그 3] answer += 1 도 for 루프 안에 있음
                                        #            약수를 찾을 때마다 중복 카운트됨

    # [❌ 버그 4] verifier가 while 루프 밖에서 딱 한 번만 초기화됨
    #            한 번 verifier=1 이 되면 다음 x에서도 1로 유지되어
    #            이후 모든 수가 합성수로 잘못 판정됨

    # [❌ 버그 5] range(2, x+1) 에서 x 자신까지 포함됨
    #            c == x 일 때 x % x == 0 이므로 모든 수가 합성수로 판정됨
    #            → range(2, x) 로 x 자신을 제외해야 함

    return answer


# ────────────────────────────────────────────────────────────
# [✅ 정답 코드] - 수정된 풀이
# ────────────────────────────────────────────────────────────

def solution(n):
    answer = 0

    # ✅ while + x+=1 대신 for문을 사용해 x를 안전하게 순회
    for x in range(2, n+1):

        verifier = 0                # ✅ [버그 4 수정] x마다 verifier를 초기화

        for c in range(2, x):      # ✅ [버그 5 수정] x 자신은 제외 → range(2, x)
            if x % c == 0:
                verifier = 1
                break              # ✅ 합성수임이 확인되면 더 볼 필요 없으므로 즉시 탈출

        # ✅ [버그 2,3 수정] for 루프가 끝난 뒤 x 전체에 대해 판단하고 카운트
        if verifier == 1:
            answer += 1

    return answer


# ────────────────────────────────────────────────────────────
# [📝 개선 포인트 요약]
# ────────────────────────────────────────────────────────────
# 1. range[]  →  range()   : 함수/클래스 호출은 반드시 소괄호
# 2. verifier 초기화 위치  : 반복문 안쪽(x마다)에서 초기화해야 함
# 3. range(2, x+1)  →  range(2, x)  : 자기 자신으로 나누는 경우 제외
# 4. x+=1, answer+=1 위치  : for c 루프 밖, while/for x 루프 안에 위치해야 함
# 5. break 추가            : 합성수 판정 즉시 탈출해 불필요한 연산 제거
