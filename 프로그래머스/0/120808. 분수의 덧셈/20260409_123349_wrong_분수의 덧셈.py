# 문제: 두 분수의 합을 기약분수로 반환
# =========================================================


# 1. 실수 수정 버전 (내 방식 유지)
def solution(numer1, denom1, numer2, denom2):
    
    if denom1 > denom2:
        if denom1 % denom2 == 0:  # 🔧 denom1 % denom1 → 오타 수정
            a = numer1 + (numer2 * (denom1 // denom2))  # 🔧 / → //
            b = denom1
        else:
            a = (numer1 * denom2) + (numer2 * denom1)
            b = denom1 * denom2
            
    elif denom1 < denom2:
        if denom2 % denom1 == 0:
            a = (numer1 * (denom2 // denom1)) + numer2
            b = denom2
        else:
            a = (numer1 * denom2) + (numer2 * denom1)
            b = denom1 * denom2
        
    else:  # denom1 == denom2
        a = numer1 + numer2
        b = denom1

    # 🔥 기약분수 처리 (필수)
    def gcd(x, y):
        while y:
            x, y = y, x % y
        return x
    
    g = gcd(a, b)
    
    return [a // g, b // g]


