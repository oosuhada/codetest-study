# =========================================
# 문자열 처리 핵심 패턴 정리 
# =========================================

# 📌 문자열은 immutable (변경 불가)
# → 직접 수정 불가능 → "슬라이싱 + 이어붙이기"로 처리해야 함

# -----------------------------------------
# 1️⃣ 문자열 슬라이싱 기본 개념
# -----------------------------------------
# 문자열[start:end]
# → start부터 end "직전까지" 잘라냄

s = "abcdef"

print(s[0:3])  # "abc"
print(s[:3])   # "abc" (start 생략 = 0)
print(s[3:])   # "def" (end 생략 = 끝까지)

# -----------------------------------------
# 2️⃣ 문자열 삽입 (중간에 끼워넣기)
# -----------------------------------------
# 패턴:
# str[:i] + new + str[i:]

s = "abcdef"
new = "ZZZ"
i = 3

result = s[:i] + new + s[i:]

print(result)  # "abcZZZdef"

# 📌 설명
# s[:3]  → "abc"
# s[3:]  → "def"
# 사이에 new 삽입

# -----------------------------------------
# 3️⃣ 문자열 삭제 (일부 구간 제거)
# -----------------------------------------
# 패턴:
# str[:i] + str[i+k:]

s = "abcdef"
i = 2   # 시작 위치
k = 3   # 삭제 길이

result = s[:i] + s[i+k:]

print(result)  # "abf"

# 📌 설명
# "cde" 제거됨

# -----------------------------------------
# 4️⃣ 문자열 교체 (코테 단골 ⭐)
# -----------------------------------------
# 패턴:
# str[:i] + replace + str[i+len(replace):]

s = "abcdef"
replace = "ZZZ"
i = 2

result = s[:i] + replace + s[i+len(replace):]

print(result)  # "abZZZf"

# 📌 설명
# 기존 "cde" → "ZZZ"로 교체

# -----------------------------------------
# 5️⃣ len() 함수 (길이 구하기)
# -----------------------------------------
# 문자열 길이 필요할 때 사용

s = "hello"
print(len(s))  # 5

# -----------------------------------------
# 6️⃣ 문자열 반복 처리 (for문)
# -----------------------------------------
# 한 글자씩 처리할 때 사용

s = "abc"

for c in s:
    print(c)

# 출력:
# a
# b
# c

