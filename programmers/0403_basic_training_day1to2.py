# ==============================
# print에서 콤마(,) vs f-string 원리 정리
# ==============================

# ------------------------------
# 1️⃣ 콤마(,) 방식
# ------------------------------
# print()는 여러 값을 "쉼표(,)"로 구분해서 넣으면
# 자동으로 문자열로 변환 + 공백을 사이에 넣어서 출력해줌

a, b = 4, 5  # 예시 값

print(a, "+", b, "=", a + b)
# 내부 동작 개념:
# print(str(a) + " " + "+" + " " + str(b) + " " + "=" + " " + str(a+b))

# 실제 출력:
# 4 + 5 = 9

# 📌 핵심 특징
# - 자동 형변환 (int → str)
# - 기본 구분자: 공백 (" ")
# - 빠르고 안전해서 코딩테스트에 적합

# 📌 sep 옵션으로 구분자 변경 가능
print(a, "+", b, "=", a + b, sep="")  
# 출력: 4+5=9 (공백 제거)


# ------------------------------
# 2️⃣ f-string 방식
# ------------------------------
# 문자열 안에 변수를 직접 넣는 방식 (Python 3.6+)
# {} 안에 변수나 연산식을 넣으면 결과가 문자열로 변환되어 들어감

print(f"{a} + {b} = {a + b}")

# 내부 동작 개념:
# "문자열" + str(a) + "문자열" + str(b) + ...

# 📌 핵심 특징
# - {} 안에서 계산 가능
# - 가독성 매우 좋음
# - 실무에서 가장 많이 사용

# 📌 다양한 활용 예시
name = "Gabriel"
age = 30

print(f"이름: {name}, 나이: {age}")
# 출력: 이름: Gabriel, 나이: 30

print(f"{a} + {b} = {a + b}, 두 수의 곱은 {a * b}")
# 출력: 4 + 5 = 9, 두 수의 곱은 20


# ------------------------------
# 콤마 vs f-string 차이 정리
# ------------------------------

# ✔ 콤마 방식
print(a, b)
# → 자동 공백 포함 출력: "4 5"

# ✔ f-string
print(f"{a}{b}")
# → 공백 없음: "45"

# ✔ 콤마는 sep으로 제어 가능
print(a, b, sep=", ")
# → "4, 5"


# ------------------------------
