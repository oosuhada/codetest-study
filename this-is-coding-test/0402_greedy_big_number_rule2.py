# 1) 누적 합
result = 0

for i in range(5):
    result += i

print("누적합:", result)


# 2) 최대값 찾기
numbers = [3, 7, 2, 9, 5]

max_value = numbers[0]  # 첫 값으로 초기화 (중요)

for num in numbers:
    if num > max_value:
        max_value = num

print("최대값:", max_value)


# 3) 조건 반복 (입력 기반)
while True:
    num = int(input("숫자 입력 (0 입력 시 종료): "))
    if num == 0:
        break
    print("입력값:", num)


