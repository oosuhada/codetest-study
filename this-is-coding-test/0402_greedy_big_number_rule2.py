# 1. 반복문을 사용하는 이유
# --------------------------------
# 같은 작업을 여러 번 해야 할 때 사용
# 매번 직접 작성하면 비효율적이고 코드가 길어짐

# ❌ 반복문 없이
print(1)
print(2)
print(3)
print(4)
print(5)

# ✅ 반복문 사용
for i in range(1, 6):
    print(i)
# → 코드가 간결해지고 유지보수 쉬움


# --------------------------------
# 2. 반복문은 언제 사용하는가
# --------------------------------

# ✔ 1) 여러 데이터 처리 (리스트 순회)
numbers = [1, 2, 3, 4, 5]

for num in numbers:
    print(num)

# ✔ 2) 같은 작업을 여러 번 반복
for i in range(3):
    print("hello")

# ✔ 3) 조건에 따라 반복 (멈출 때까지)
while True:
    print("계속 실행")
    break  # 무한 반복 방지용 (한 번만 실행)

# ✔ 4) 누적 계산
result = 0

for i in range(1, 6):
    result += i  # result = result + i


# --------------------------------
# 3. 반복문의 2가지 종류
# --------------------------------

# 1) for문 (횟수/범위 기반)
# 반복 횟수가 정해져 있을 때 사용

for i in range(3):
    print(i)

# 실행 결과
# 0
# 1
# 2


# 2) while문 (조건 기반)
# 반복 횟수가 정해져 있지 않을 때 사용

i = 0

while i < 3:
    print(i)
    i += 1  # 반드시 변화 필요!


# --------------------------------
# 4. 반복문의 구성 원리 (매우 중요 ⭐)
# --------------------------------

# 반복문은 항상 3가지 요소 필요
# 1) 시작(초기값)
# 2) 조건
# 3) 변화(증감식)

i = 0           # 시작

while i < 5:    # 조건
    print(i)
    i += 1      # 변화

# 3개 중 하나라도 없으면:
# → 무한 반복 또는 실행 안 됨


# --------------------------------
# 5. 반복문 제어
# --------------------------------

# ✔ break → 반복문 즉시 종료
while True:
    if True:
        break

# ✔ 특정 조건에서 종료
m = 3

while True:
    if m == 0:
        break
    m -= 1

# ✔ k번 반복
k = 3
for i in range(k):
    print("k번 반복")


