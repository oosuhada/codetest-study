# N, M, K를 공백 기준으로 입력받아서 각각 변수에 저장
# map(int, input().split())
# → input() : 한 줄 입력 (문자열)
# → .split() : 공백 기준으로 나눔
# → map(int, ...) : 각각을 정수로 변환
n, m, k = map(int, input().split())

# N개의 숫자를 입력받아서 리스트로 저장
# list(...) : map 객체를 실제 리스트로 변환
data = list(map(int, input().split()))

# 입력받은 숫자들을 오름차순 정렬
# 예: [2, 4, 5, 4, 6] → [2, 4, 4, 5, 6]
data.sort()

# 가장 큰 수 (리스트의 마지막 요소)
# 인덱스는 0부터 시작하므로 n-1이 마지막
first = data[n - 1]
# data[-1]로도 접근 가능 (뒤에서 첫 번째 요소)

# 두 번째로 큰 수
second = data[n - 2]
# data[-2]로도 접근 가능 (뒤에서 두 번째 요소)

# 오름차순 대신 내림차순으로 정렬할 수도 있음
# data.sort(reverse=True)
# first = data[0]
# second = data[1]

# 사실 데이터 정렬 없이도 최대값과 두 번째 최대값을 찾을 수 있음
# 그러나 내부적으로 두번 반복해서 리스트를 탐색해야 하므로 시간 복잡도가 O(n) + O(n) = O(2n)
# first = max(data)
# data.remove(first)
# second = max(data)

# 질문 사항 - “최대값이 한 번만 나온다”는 조건은 없지 않나?
# 예: [2, 4, 4, 6, 6] → 최대값 6이 두 번 나옴
# 이 경우에도 가장 큰 수는 6, 두 번째로 큰 수는 4
# 그러나 정렬하면 가장 큰 수의 index가 2개가 됨
# 문제에서 조건 : 단, 서로 다른 인덱스에 해당하는 수가 같은 경우에도 서로 다른것으로 간주함


# 최종 결과를 저장할 변수
# 숫자를 하나씩 계속 더해서 최종 결과를 계산할 예정
# 중간 계산 결과를 저장하기 위한 변수로 0으로 초기화
result = 0

# 반복문 작성
# "가장 큰 수를 K번 더하고 → 그 다음 두 번째 수를 1번 더하고 → 이걸 계속 반복한다"

# while (계속 반복):
#     for (K번 반복):
#         first 더하기

#     second 더하기

# 무한 반복 시작 (while True는 조건 없이 계속 반복)
while True:

    # 가장 큰 수를 K번 더하는 반복문
    for i in range(k):  # range(k)는 0 ~ k-1까지 반복 (총 k번)
        
        # 만약 더할 횟수(m)가 0이면 더 이상 계산할 필요 없음
        if m == 0:
            break
        
        # 가장 큰 수를 결과에 더함
        result += first
        
        # 더했으므로 m(남은 횟수) 1 감소
        m -= 1

    # 위 for문 끝났을 때도 m이 0이면 종료
    if m == 0:
        break

    # 두 번째로 큰 수를 한 번 더함
    result += second
    
    # 역시 횟수 감소
    m -= 1

# 최종 결과 출력
print(result)




